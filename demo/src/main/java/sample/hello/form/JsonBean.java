package sample.hello.form;

public class JsonBean {

	Integer arm;

	Integer rear;
	Integer leg;
	Integer sub;
	/**
	 * @return arm
	 */
	public Integer getArm() {
		return arm;
	}
	/**
	 * @param arm セットする arm
	 */
	public void setArm(Integer arm) {
		this.arm = arm;
	}
	/**
	 * @return rear
	 */
	public Integer getRear() {
		return rear;
	}
	/**
	 * @param rear セットする rear
	 */
	public void setRear(Integer rear) {
		this.rear = rear;
	}
	/**
	 * @return leg
	 */
	public Integer getLeg() {
		return leg;
	}
	/**
	 * @param leg セットする leg
	 */
	public void setLeg(Integer leg) {
		this.leg = leg;
	}
	/**
	 * @return sub
	 */
	public Integer getSub() {
		return sub;
	}
	/**
	 * @param sub セットする sub
	 */
	public void setSub(Integer sub) {
		this.sub = sub;
	}

}
